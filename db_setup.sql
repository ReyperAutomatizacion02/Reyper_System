-- 7. Crear Políticas de Acceso
-- Roles: Lectura pública para autenticados
CREATE POLICY "Roles visibles para autenticados" ON public.roles
  FOR SELECT TO authenticated USING (true);

-- User Roles: Usuario ve sus propios roles
CREATE POLICY "Usuario ve sus propios roles" ON public.user_roles
  FOR SELECT TO authenticated USING (auth.uid() = user_id);

-- Perfiles: Usuario ve su propio perfil
CREATE POLICY "Usuario ve su propio perfil" ON public.profiles
  FOR SELECT TO authenticated USING (auth.uid() = id);

